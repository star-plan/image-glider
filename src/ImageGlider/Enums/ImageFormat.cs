namespace ImageGlider.Enums;

/// <summary>
/// 支持的图片格式枚举
/// </summary>
public enum ImageFormat {
    /// <summary>
    /// JPEG 格式
    /// </summary>
    Jpeg,

    /// <summary>
    /// PNG 格式
    /// </summary>
    Png,

    /// <summary>
    /// GIF 格式
    /// </summary>
    Gif,

    /// <summary>
    /// BMP 格式
    /// </summary>
    Bmp,

    /// <summary>
    /// TIFF 格式
    /// </summary>
    Tiff,

    /// <summary>
    /// WebP 格式
    /// </summary>
    WebP,

    /// <summary>
    /// AVIF 格式（高效图像格式）
    /// </summary>
    Avif,

    /// <summary>
    /// JFIF 格式
    /// </summary>
    Jfif
}